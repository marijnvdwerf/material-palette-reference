    public ColorUtilsTest(String name, String hexColor, float hue, float saturation, float lightness) {
        expectedRGB = Color.parseColor(hexColor);
        expectedHSL = new float[]{hue, saturation, lightness};
    }

    @Test
    public void RGBtoHSL() throws Exception {
        float[] actualHSL = new float[3];
        ColorUtils.RGBtoHSL(Color.red(expectedRGB), Color.green(expectedRGB), Color.blue(expectedRGB), actualHSL);

        assertThat(actualHSL[0])
                .describedAs("Hue")
                .isBetween(0f, 360f)
                .isCloseTo(expectedHSL[0], Offset.offset(0.0001f));

        assertThat(actualHSL[1])
                .describedAs("Saturation")
                .isBetween(0f, 1f)
                .isCloseTo(expectedHSL[1], Offset.offset(0.0001f));

        assertThat(actualHSL[2])
                .describedAs("Lightness")
                .isBetween(0f, 1f)
                .isCloseTo(expectedHSL[2], Offset.offset(0.0001f));
    }

    @Test
    public void HSLtoRGB() throws Exception {
        int actualRGB = ColorUtils.HSLtoRGB(expectedHSL);
        assertThat(actualRGB)
                .isEqualTo(expectedRGB);
    }

    public void testHSLLimits() {
        float[] hsl = new float[3];
        ColorUtils.RGBtoHSL(255, 233, 210, hsl);

        Assert.assertEquals(1.0, Math.ceil(hsl[1]));
        Assert.assertEquals(1.0, Math.ceil(hsl[2]));
    }
